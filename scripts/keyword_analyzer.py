def extract_keywords_from_url(url_path):
    """Извлекает ключевые слова из URL пути"""
    # Декодируем URL
    decoded = urllib.parse.unquote(url_path)
    # Убираем начальный /
    decoded = decoded.lstrip('/')
    # Разбиваем по /
    parts = decoded.split('/')
    
    keywords = []
    for part in parts:
        # Пропускаем технические части
        if part in ('', 'catalog', 'articles', 'portfolio', 'rabotyi', 'o-nas', 'stati', 'katalog', 'doma-iz-brusa', 'karkasnye-doma', 'dachnye-doma', 'doma-pod-klyuch', 'bani-iz-brusa', 'karkasnye-bani', 'proyekty', 'page', 'filters', 'print', 'reviews'):
            continue
        # Пропускаем параметры запроса
        if '?' in part or '&' in part or '=' in part:
            continue
        # Пропускаем ID страниц (pk-, dk-, db-, dd-, bk-)
        if re.match(r'^(pk|dk|db|dd|bk)-\d+', part):
            continue
        # Пропускаем технические файлы
        if part.endswith('.html') or part.endswith('.xml') or part.endswith('.docx'):
            continue
        
        # Очищаем от лишнего
        clean = part.replace('-', ' ').replace('_', ' ').strip()
        if clean and len(clean) > 2:
            keywords.append(clean.lower())
    
    return keywords

def extract_keywords_from_html():
    """Извлекает ключевые слова из title HTML страниц"""
    keywords = []
    
    html_files = list(HTML_DIR.glob("*.html"))
    for html_file in html_files:
        try:
            content = html_file.read_text(encoding='utf-8', errors='ignore')
            # Ищем title
            title_match = re.search(r'<title>(.*?)</title>', content, re.IGNORECASE | re.DOTALL)
            if title_match:
                title = title_match.group(1).strip()
                # Очищаем title
                title = re.sub(r'[\|•—–\-]+', ' ', title)
                title = re.sub(r'\s+', ' ', title).strip().lower()
                # Разбиваем на слова
                words = re.findall(r'[а-яёa-z]+', title)
                # Фильтруем стоп-слова
                stop_words = {'и', 'в', 'на', 'с', 'по', 'для', 'от', 'к', 'за', 'под', 'над', 'при', 'из', 'до', 'после', 'во', 'со', 'об', 'ко', 'про', 'без', 'нас', 'вас', 'они', 'мы', 'вы', 'он', 'она', 'оно', 'это', 'так', 'как', 'что', 'где', 'когда', 'кто', 'почему', 'сколько', 'сайт', 'главная', 'страница', 'ru', 'com', 'www', 'http', 'https', 'sk', 'tu', 'msk'}
                for word in words:
                    if len(word) > 3 and word not in stop_words:
                        keywords.append(word)
        except Exception as e:
            print(f"Error reading {html_file}: {e}")
    
    return keywords

def analyze_csv_files():
    """Анализирует CSV файлы с URL"""
    all_keywords = []
    
    csv_files = list(RAW_DIR.glob("*.csv"))
    print(f"Found {len(csv_files)} CSV files")
    
    for csv_file in csv_files:
        try:
            with open(csv_file, 'r', encoding='utf-8', errors='ignore') as f:
                reader = csv.DictReader(f)
                for row in reader:
                    url = row.get('url', '')
                    if url:
                        keywords = extract_keywords_from_url(url)
                        all_keywords.extend(keywords)
        except Exception as e:
            print(f"Error reading {csv_file}: {e}")
    
    return all_keywords

def get_top_keywords(keywords, n=50):
    """Возвращает топ-N ключевых слов"""
    counter = Counter(keywords)
    return counter.most_common(n)

